                        <h4 style="color: #F33;">(Q) How long can blood be preserved in a blood bank?</h4>
                        <p align="justify">(A) In India blood is normally preserved in blood bank for 35 days using CPDA or CPDA1 solution as anticoagulant. If additive solution is added to Red Blood Cells after separation, the shelf life increases to 42 days.</p>
                        
                        <hr>
                        
                        <h4 style="color: #F33;">(Q) The maximum life span of RBC is 120 days. Why blood is then is preserved in blood bank for only 35 days?</h4>
                        <p align="justify">(A) Donated blood contains cells ranging from 1 to 120 days' life span. Naturally, cells having such varied life spans cannot be preserved for 120 days in the bag since in that case there will only be a few living cells present. In order to get a considerable number of living cells present in the blood for transfusion, 35 days shelf life has been found to be optimum with CPDA and CPDA1 solution on the basis of various experiments. Furthermore, cells which can remain alive for 120 days within the human body cannot survive for such period in an artificial environment.</p>
                        
                        <hr>
                        
                        <h4 style="color: #F33;">(Q) Will there be any good if blood is transfused to a patient on the 34th day after collection from the donor?</h4>
                        <p align="justify">(A) There will still be a considerable number of living red cells present in the bag which will be able to carry oxygen to the cells and bring back carbon dioxide.</p>
                        
                        <hr>
                        
                        <h4 style="color: #F33;">(Q) Often on tendering the blood donor's card, blood is not being made available from the blood banks and then what is the use of donating blood?</h4>
                        <p align="justify">(A) Donation in the real sense of the term is unconditional and without any string. Further, the credit card is given to the donor as a recognition and to meet the donor's own future need or the need of the donor's near relatives, who have no other alternative but to depend only on the donor to meet his/her blood need. It is expected that 10-15% donor cards would be tendered. If all the cards are tendered, then blood banks would not be able to honor cards in view of existing gap between demand and supply, as blood banks have to issue blood to serious patients also who have no cards at all. One should remember blood donors are not depositors. If there are enough donors, everybody should get blood in time of their need.</p>
                        
